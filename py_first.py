class Cat:
    '''这是一个猫类'''

    def eat(self):
        print("小猫爱吃鱼")

    def drink(self):
        print("小猫在喝水")
#创建一个猫类
#等号右边创建对象，等号左边对新创建的对象进行引用
tom=Cat()
tom.drink()
tom.eat()
#输出tom变量的内存地址
print(tom)

#再创建一个猫类
lazy_cat=Cat()
lazy_cat.drink()
lazy_cat.eat()
print(lazy_cat)

